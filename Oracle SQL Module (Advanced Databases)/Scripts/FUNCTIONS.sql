CREATE FUNCTION EVENTNAME
    (ID IN VARCHAR)
    RETURN VARCHAR
IS
    NAME VARCHAR(50);
    CURSOR A IS
        SELECT EVENT_NAME
        FROM EVENT
        WHERE EVENT_ID = ID;

BEGIN

OPEN A;
FETCH A INTO NAME;

IF A%NOTFOUND THEN
    NAME := 'NOT FOUND';
END IF;
CLOSE A;

RETURN NAME;

END;

/

SELECT EVENT_ID, EVENTNAME('1003') AS "EVENT"
FROM EVENT
WHERE EVENT_ID = '1003';